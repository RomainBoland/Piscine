void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	o;
	int	tmp;

	i = 0;
	o = size - 1;
	while (tab[i] <= size / 2)
	{
		tmp = tab[i];
		tab[i] = tab[o];
		tab[o] = tmp;
		i++;
		o--;
	}
}
